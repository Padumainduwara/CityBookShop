    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isValid = false;
        
        if (txtManagerId.getText().isEmpty() || txtPassword.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(null,"Login credentials cannot be blank." ,"Login", JOptionPane.ERROR_MESSAGE);
        } 
        else 
        {
            String ManagerId = txtManagerId.getText();
            String Password = txtPassword.getText();
            
            ManagerClass Mn = new ManagerClass(ManagerId, Password);
            if (Mn.validateLogin()) {
                JOptionPane.showMessageDialog(null,"Login Successfull" ,"Login", 
                        JOptionPane.INFORMATION_MESSAGE);
            
               ManagerInter Mi = new ManagerInter();            
               Mi.show();
               this.dispose();
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Login Fail" ,"Login",
                        JOptionPane.WARNING_MESSAGE);
                
                MainInterface Mi = new MainInterface();
                    Mi.show();
                    this.dispose();
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

