    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         String keyword = txtEnterBookId.getText();
        
        if (keyword.isEmpty()) 
        {
        JOptionPane.showMessageDialog(null,"Sorry! Have to enter Employee ID" ,
                "Search a Employee", JOptionPane.ERROR_MESSAGE);
        
        } 
        else
        {
            BookClass bk = new BookClass();
            String bkTableTitle = "BookId \t Name \t Category \t Author Name \t Department \n ------------------------------------------------------------------------------------------------------------ \n";
            if (bk.searchBooks(keyword)) 
            {
          txtDisplayBooks.setText(bkTableTitle + bk.getBookId()+ "\t" + bk.getBookName()+ "\t" + 
                        bk.getCategory()+ "\t" + bk.getAuthorName()+ "\t" + bk.getPrice());
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Sorry! We do not have any matching Employee" ,"Search a Employee", 
                        JOptionPane.ERROR_MESSAGE);
            }
    }                     
    }//GEN-LAST:event_btnSearchActionPerformed

