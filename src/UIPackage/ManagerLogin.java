    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isValid = false;
        
        if (txtManagerId.getText().isEmpty() || txtPassword.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(null,"Login credentials cannot be blank." 
                    ,"Login", JOptionPane.ERROR_MESSAGE);
        } 
        else 
        {
            String managerId = txtManagerId.getText();
            String password = txtPassword.getText();
            
            ManagerClass mn = new ManagerClass(managerId, password);
            if (mn.validateLogin()) {
                JOptionPane.showMessageDialog(null,"Login Successfull" ,"Login ", 
                        JOptionPane.INFORMATION_MESSAGE);
            
               ManagerInter main = new ManagerInter();            
               main.show();
               this.dispose();
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Login Fail" 
                        ,"Login", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

