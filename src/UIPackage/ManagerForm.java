    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (txtManagerName.getText().isEmpty() || txtManagerId.getText().isEmpty() || txtPassword.getText().isEmpty() ||
               txtConfirmPassword.getText().isEmpty()) 
        {
                JOptionPane.showMessageDialog(null,"Fileds cannot be empty, When adding a new manager." ,
                        "Add a new User", JOptionPane.WARNING_MESSAGE);
        }
        else 
        {  
                String Managername = txtManagerName.getText();
                String Managerid = txtManagerId.getText();
                String Password = txtPassword.getText();
                

                ManagerClass newManager = new ManagerClass(Managername,Managerid,Password);

                if (newManager.addNewManager()) 
                {
                    JOptionPane.showMessageDialog(null,"Manager Added successfully" ,"Add a new Manager", 
                            JOptionPane.INFORMATION_MESSAGE);
                    
                  ManagerLogin Ml = new ManagerLogin();            
                  Ml.show();
                  this.dispose();
                  }
                else 
                {
                    JOptionPane.showMessageDialog(null,"Sorry! Something went wrong with the application, "
                            + "Will be fixing it soon." ,"Add a new User",
                            JOptionPane.WARNING_MESSAGE);
                    
                    MainInterface Mi = new MainInterface();
                    Mi.show();
                    this.dispose();
                }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

