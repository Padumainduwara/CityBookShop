    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
           boolean isValid = false;
        
        if (txtCashierId.getText().isEmpty() || txtPassword.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(null,"Login credentials cannot be blank." ,"Login", JOptionPane.ERROR_MESSAGE);
        } 
        else 
        {
            String CashierId = txtCashierId.getText();
            String password = txtPassword.getText();
            
            CashierClass Ca = new CashierClass(CashierId, password);
            if (Ca.validateLogin()) {
                JOptionPane.showMessageDialog(null,"Login Successfull" ,"Login", 
                        JOptionPane.INFORMATION_MESSAGE);
            
               CashierInter Ci = new CashierInter();            
               Ci.show();
               this.dispose();
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Login Fail" ,"Login", JOptionPane.WARNING_MESSAGE);
                
                MainInterface Mi = new MainInterface();
                    Mi.show();
                    this.dispose();
            
            }
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

